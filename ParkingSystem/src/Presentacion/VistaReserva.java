    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Calendar calendar;
        int dia, mes, anio;

        //Fecha reserva
        calendar = dcFechaReserva.getCalendar();
        dia = calendar.get(Calendar.DAY_OF_MONTH);
        mes = calendar.get(Calendar.MONTH);
        anio = calendar.get(Calendar.YEAR) - 1900;
        Date fecha = new Date(anio, mes, dia);

        //indices de valor de combo
        int seleccionado1 = cboEspacio.getSelectedIndex();
        int seleccionado2 = cboHoraInicio.getSelectedIndex();
        int seleccionado3 = cboHoraFin.getSelectedIndex();

        //valores de combos
        String hI = (String) cboHoraInicio.getItemAt(seleccionado2);
        String hF = (String) cboHoraFin.getItemAt(seleccionado3);
        int resultado = Integer.parseInt(hF) - Integer.parseInt(hI);
        
        if (resultado > 4 || resultado < 3) {
            JOptionPane.showMessageDialog(rootPane, "Reserva fuera de rango establecido", "Error!!!", JOptionPane.ERROR_MESSAGE);
        } else {
            Reserva objNewReserva = new Reserva(String.valueOf(fecha), hI+":00", hF+":00", "reservado", VistaLogin.idUsuario, idEspacio);
            LogicaReserva objReserva = new LogicaReserva();
            try {
                objReserva.guardarReserva(objNewReserva);
                LogicaEspacio objEspacio= new LogicaEspacio();
                objEspacio.ocuparEspacio(idEspacio);
                JOptionPane.showMessageDialog(rootPane, "Reserva Guardada exitosamente!!!", "Guardado", JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException ex) {
                Logger.getLogger(VistaReserva.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(VistaReserva.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

    private ArrayList<Espacio> llenarCombo() {
        ArrayList<Espacio> listEspacio = new ArrayList<Espacio>();
        LogicaReserva objEspacio = new LogicaReserva();
        try {
            listEspacio = objEspacio.obtenerEspacios(VistaLogin.espacio);
            DefaultComboBoxModel dlm = new DefaultComboBoxModel();
            for (Espacio t : listEspacio) {
                dlm.addElement(t.getCodigo());
                this.idEspacio=t.getIdEspacio();
            }
            cboEspacio.setModel(dlm);
        } catch (Exception e) {
            e.printStackTrace();
        }
        return listEspacio;
    }

